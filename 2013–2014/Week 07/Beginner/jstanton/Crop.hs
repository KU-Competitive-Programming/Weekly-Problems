{-
  Crop the matrix!
-}

crop []   = []
crop strs = map (\s -> take takeRight $ drop dropLeft s) strs' where  
  strs'     = drop0strings . reverse . drop0strings . reverse $ strs
  dropLeft  = minimum $ get0s strs' 
  takeRight = (length $ head strs') - dropLeft - (minimum $ get0s $ reverse strs')  
  drop0strings = dropWhile (not . elem '1')
  get0s = map length . map (takeWhile (/='1'))
  
      
